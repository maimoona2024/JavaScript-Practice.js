//************************************************VERY SIMPLE CALCULATOR IN JS***************************************************
let a = 6, b = 5;
let operation = "+";
if(operation == "+") {
    console.log("Addition:",a+b);
}
else if(operation == "-") {
    console.log("Subtraction: ",a-b);
}
else if(operation == "*") {
    console.log("Multiplication:",a*b);
}
else if(operation == "/") {
    console.log("Division:",a/b);
}
else console.log("Get out!");
